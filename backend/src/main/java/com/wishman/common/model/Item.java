package com.wishman.common.model;

public class Item {

	String kid_name;
	String kid_pic;
	String produc_name;
	String produc_url;
	String produc_id;
	String produc_img;
	Long product_price;
	String kid_gender;
	
	public String getKid_name(){
		return kid_name;
	}
	public void setKid_name(String kid_name){
		this.kid_name = kid_name;
	}
	public String getKid_pic(){
		return kid_pic;
	}
	public void setKid_pic(String kid_pic){
		this.kid_pic = kid_pic;
	}
	public String getProduc_name(){
		return produc_name;
	}
	public void setProduc_name(String produc_name){
		this.produc_name = produc_name;
	}
	public String getProduc_url(){
		return produc_url;
	}
	public void setProduc_url(String produc_url){
		this.produc_url = produc_url;
	}
	public String getProduc_id(){
		return produc_id;
	}
	public void setProduc_id(String produc_id){
		this.produc_id = produc_id;
	}
	public String getProduc_img(){
		return produc_img;
	}
	public void setProduc_img(String produc_img){
		this.produc_img = produc_img;
	}
	public Long getProduct_price(){
		return product_price;
	}
	public void setProduct_price(Long product_price){
		this.product_price = product_price;
	}
	public String getKid_gender(){
		return kid_gender;
	}
	public void setKid_gender(String kid_gender){
		this.kid_gender = kid_gender;
	}	
	
}